// Test script to deeply search for verification and operational fields
const fs = require('fs');
const path = require('path');

// Deep search function
const deepSearch = (obj, targetKeys, path = '') => {
  const results = [];

  for (const key in obj) {
    if (!obj.hasOwnProperty(key)) continue;
    const currentPath = path ? `${path}.${key}` : key;

    // Check if this key matches any target
    const lowerKey = key.toLowerCase();
    const isMatch = targetKeys.some(target => lowerKey.includes(target));

    if (isMatch) {
      results.push({
        path: currentPath,
        value: typeof obj[key] === 'object' ? '[Object]' : obj[key],
        type: typeof obj[key]
      });
    }

    // Recursively search nested objects
    if (typeof obj[key] === 'object' && obj[key] !== null) {
      const nestedResults = deepSearch(obj[key], targetKeys, currentPath);
      results.push(...nestedResults);
    }
  }

  return results;
};

// Read and analyze data files
console.log('=== Deep Search for Verification and Operational Fields ===\n');

// Target keys to search for
const targetKeys = ['verification', 'operational', 'context', 'steps', 'note'];

// 1. Search enforcer pattern library
try {
  const enforcerPath = path.join(__dirname, 'data', 'enforcer_pattern_library_v2.0.json');
  const enforcerData = JSON.parse(fs.readFileSync(enforcerPath, 'utf8'));

  console.log('Searching enforcer pattern library...');
  const enforcerResults = deepSearch(enforcerData, targetKeys);

  console.log(`Found ${enforcerResults.length} matches`);

  // Group by path prefix
  const grouped = {};
  enforcerResults.forEach(result => {
    const prefix = result.path.split('.').slice(0, 3).join('.');
    if (!grouped[prefix]) grouped[prefix] = [];
    grouped[prefix].push(result);
  });

  console.log('\nTop matches:');
  Object.keys(grouped).slice(0, 10).forEach(prefix => {
    console.log(`\n${prefix}:`);
    grouped[prefix].forEach(result => {
      console.log(`  - ${result.path} (${result.type})`);
    });
  });

} catch (err) {
  console.log(`Error: ${err.message}`);
}

console.log('\n---\n');

